                        <h2 class="title-subtitle text-subtitle-little">Apenas 1 em 4 pessoas com doença de Alzheimer é diagnosticada4</h2>

                        <p class="news-info">
                            É preciso repensar a forma que a DA é definida, a fim de melhorar a capacidade de detectar 
                            e identificar os sinais e sintomas iniciais da doença, promovendo o diagnóstico precoce e correto. 
                            O quanto antes os sintomas são reconhecidos, o quanto antes medidas são tomadas. 
                        </p>

                        <p class="news-info">
                            É importante ter em mente que a doença de Alzheimer age silenciosa e lentamente até causar 
                            a lesão cerebral – inclusive, as placas de beta-amiloide podem começar a se formar de 10 a 20 
                            anos antes dos primeiros sintomas¹¹. O comprometimento cognitivo leve também é um dos estágios 
                            iniciais da DA, contudo, muitos profissionais de saúde não reconhecem essa fase como parte do curso 
                            da doença. Ou seja, a prática clínica atual não reconhece que uma pessoa tenha doença de Alzheimer até 
                            que o caso esteja avançado e a demência, evidente¹². 
                        </p>

                        <p class="news-info">
                            O acesso à informação de qualidade, de fácil compreensão e com respaldo científico, pode contribuir 
                            para o diagnóstico – sobretudo o precoce. Não à toa, este canal convida a todos a repensarem o 
                            seu papel no reconhecimento das primeiras manifestações da doença de Alzheimer. 
                        </p>

