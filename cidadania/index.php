                        <h2 class="title-subtitle text-subtitle-little">
                            Qual o cenário mundial?
                        </h2>

                        <p class="news-info">
                            Desde 2015, a Organização Pan-Americana da Saúde (OPAS) estabeleceu que os países membros 
                            desenvolvessem planos para lidar com a doença². Entretanto, apenas quatro (Argentina, Chile, Costa Rica e México) 
                            dos 51 países e territórios representados pela OPAS formularam estratégias contra demência³. 
                        </p>
                        
                        <p class="news-info">
                            Atualmente, o Brasil não conta com um plano nacional para demência e a doença integra o rol mais 
                            abrangente do sistema de saúde, como Constituição Federal, Política Nacional de Saúde da Pessoa Idosa e Estatuto do Idoso³. 
                            Apesar de proteger os idosos, fornecer subsídios que garantam sua participação na comunidade, defender sua dignidade e zelar 
                            por seu bem-estar, as políticas públicas atuais são pouco específicas sobre demências em geral³. 
                        </p>

                        <p class="news-info">
                            Em 2018, a Global Coalition on Aging (GCOA) e Alzheimer’s Disease International (ADI) produziram um Índice de Prontidão 
                            para Inovação em Demência comparando Argentina, Brasil, China, Índia e Arábia Saudita. Os países foram escolhidos diante de 
                            sua liderança na reunião do G20, encontro das 20 maiores economias do mundo, em 2020, e pelo fato de que a demência aumentará 
                            de forma significativa em países em desenvolvimento. No ranking, o Brasil teve o pior desempenho⁴. 
                        </p>

                        <img class="news-container-image news-container-image--no-margin-top" src="../assets/images/cidadania/grafico.svg" alt="">

