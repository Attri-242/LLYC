                        <h2 class="title-subtitle text-subtitle-little">
                            É possível detectar a doença de Alzheimer ainda no estágio pré-clínico?⁵
                        </h2>
    
                        <p class="news-info">
                            A doença de Alzheimer está associada ao acúmulo das proteínas beta-amilóides e tau no cérebro 
                            e o excesso dessas moléculas pode surgir até 20 anos antes dos primeiros sintomas clínicos. Para identificar 
                            a DA ainda na etapa pré-clínica é preciso utilizar biomarcadores, que detectam níveis anormais dessas proteínas 
                            e têm papel importante para o diagnóstico da doença de Alzheimer.
                        </p>
    
                        <div class="action-card">
                            <p class="action-card-text action-card-text--no-margin-top">
                                Antigamente, as diretrizes para doença de Alzheimer abordavam apenas a demência como estágio da 
                                doença sendo a perda de memória sua característica principal. Atualmente, são reconhecidos cinco estágios de DA: 
                                pré-clínico, comprometimento cognitivo leve e demência leve, moderada e severa. Sabe-se também que aspectos da cognição, 
                                como capacidade de encontrar palavras ou julgamento, podem ser prejudicados antes da memória.¹³
                            </p>
    
                            <p class="action-card-text action-card-text--no-margin-top">
                                O diagnóstico precoce contribui para o melhor manejo da doença. É também uma oportunidade
                                para iniciar algumas práticas de saúde: exercícios físicos, atividades mentais e envolvimento social.
                            </p>
                        </div>

